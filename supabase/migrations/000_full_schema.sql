CREATE INDEX IF NOT EXISTS idx_focus_state_user_date ON today_focus_state (user_id, focus_date);

-- ── 今日手动事件 ──
CREATE TABLE IF NOT EXISTS today_focus_manual (
  id         BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_id    UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  focus_date TEXT NOT NULL,
  type       TEXT NOT NULL DEFAULT '系统',
  title      TEXT NOT NULL,
  note       TEXT DEFAULT '',
  created_at TIMESTAMPTZ DEFAULT now(),
  updated_at TIMESTAMPTZ DEFAULT now(),
  soft_deleted BOOLEAN DEFAULT FALSE
);

CREATE INDEX IF NOT EXISTS idx_focus_manual_user_date ON today_focus_manual (user_id, focus_date, soft_deleted);

-- ── 应用设置 ──
CREATE TABLE IF NOT EXISTS app_settings (
  user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
  key     TEXT NOT NULL,
  value   TEXT DEFAULT '',
  PRIMARY KEY (user_id, key)
);


-- ┌─────────────────────────────────────────────────────────────────────┐
-- │  2. ROW LEVEL SECURITY                                             │
-- └─────────────────────────────────────────────────────────────────────┘

DO $$
DECLARE
  tbl TEXT;
BEGIN
  FOR tbl IN
    SELECT unnest(ARRAY[
      'leads', 'clients', 'tasks', 'plans', 'finance_transactions',
      'content_drafts', 'activity_log', 'client_subscription_ledger',
      'today_focus_state', 'today_focus_manual', 'app_settings',
      'payment_milestones'
    ])
  LOOP
    EXECUTE format('ALTER TABLE %I ENABLE ROW LEVEL SECURITY', tbl);
    EXECUTE format(
      'CREATE POLICY "users_own_data_%1$s" ON %1$I FOR ALL USING (auth.uid() = user_id) WITH CHECK (auth.uid() = user_id)',
      tbl
    );
  END LOOP;
END $$;


-- ┌─────────────────────────────────────────────────────────────────────┐
-- │  3. AUTO-UPDATE updated_at 触发器                                   │
-- └─────────────────────────────────────────────────────────────────────┘

CREATE OR REPLACE FUNCTION update_updated_at()
RETURNS TRIGGER AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$ LANGUAGE plpgsql;

DO $$
DECLARE
  tbl TEXT;
BEGIN
  FOR tbl IN
    SELECT unnest(ARRAY[
      'leads', 'clients', 'tasks', 'plans', 'finance_transactions',
      'content_drafts', 'today_focus_state', 'today_focus_manual',
      'payment_milestones'
    ])
  LOOP
    EXECUTE format(
      'CREATE TRIGGER trg_updated_at_%1$s BEFORE UPDATE ON %1$I FOR EACH ROW EXECUTE FUNCTION update_updated_at()',
      tbl
    );
  END LOOP;
END $$;


-- ┌─────────────────────────────────────────────────────────────────────┐
-- │  4. REALTIME                                                       │
-- └─────────────────────────────────────────────────────────────────────┘

DO $$
DECLARE
  tbl TEXT;
BEGIN
  FOR tbl IN
    SELECT unnest(ARRAY[
      'leads', 'clients', 'tasks', 'plans', 'finance_transactions',
      'content_drafts', 'today_focus_state', 'today_focus_manual',
      'payment_milestones'
    ])
  LOOP
    BEGIN
      EXECUTE format('ALTER PUBLICATION supabase_realtime ADD TABLE %I', tbl);
    EXCEPTION WHEN duplicate_object THEN
      NULL;
    END;
  END LOOP;
END $$;
